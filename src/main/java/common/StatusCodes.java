package common;

/**
 * This class holds a list of common status codes returned by REST service
 * 
 * @author Deepa
 *
 */
public final class StatusCodes {

	public final static int redirect = 302;
	public final static int ok = 200;
	public final static int badrequest = 400;
	public final static int servererror = 500;
	public final static int unsupportedMediaType = 415;
	public final static int methodNotAllowed = 405;
	public final static int notFound = 404;

}
